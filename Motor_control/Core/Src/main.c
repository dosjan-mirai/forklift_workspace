/**
 * @brief  0–255 joystick утгыг float позиц руу шугаман хөрвүүлэх.
 *         127 = төв (deadband дотор) → pos_center буцаана.
 *         0   = pos_low,  255 = pos_high
 */
/*
 * @brief  0.0–1.0 хэмжилтийг pos_released → pos_engaged хооронд хөрвүүлэх
 */
static float lerp(float t, float from, float to)
{
	return from + t * (to - from);
}

/**
 * @brief  Joystick нэг тэнхлэгийг acc/brake хоёр мотор руу хуваарилах.
 *
 *  raw > center+deadband  →  Acc дарна (t=0→1),  Brake бүрэн тавина
 *  raw < center-deadband  →  Brake дарна (t=0→1), Acc бүрэн тавина
 *  deadband дотор         →  Хоёулан тавина
 */
static void apply_joystick(uint8_t raw)
{
    /* ── Acc / Brake: RX[6] ─────────────────────── */
    float acc_pos, brake_pos;

    if (raw < (JOY_CENTER - JOY_DEADBAND))
    {
        float t = (float)((JOY_CENTER - JOY_DEADBAND) - raw) /
                  (float)(JOY_CENTER - JOY_DEADBAND);
        acc_pos   = lerp(t, ACC_POS_RELEASED, ACC_POS_ENGAGED);
        brake_pos = BRAKE_POS_RELEASED;
    }
    else if (raw > (JOY_CENTER + JOY_DEADBAND))
    {
        float t = (float)(raw - (JOY_CENTER + JOY_DEADBAND)) /
                  (float)(255U - (JOY_CENTER + JOY_DEADBAND));
        brake_pos = lerp(t, BRAKE_POS_RELEASED, BRAKE_POS_ENGAGED);
        acc_pos   = ACC_POS_RELEASED;
    }
    else
    {
        acc_pos   = ACC_POS_RELEASED;
        brake_pos = BRAKE_POS_RELEASED;
    }

    (void)RobStride_WriteFloat(motor_acc,   ROBSTRIDE_IDX_LOC_REF, acc_pos);
    (void)RobStride_WriteFloat(motor_brake, ROBSTRIDE_IDX_LOC_REF, brake_pos);

    /* ── Steering: RX[7] ───────────────────────── */
    int16_t steer_raw = (int16_t)JOY_STEER - 127;

    if (steer_raw > -10 && steer_raw < 10)
    {
        steer_raw = 0;
    }

    float steer_pos = (float)steer_raw * (STEER_MAX_POS / 127.0f);

    Steering_motor_cmd.K_P = 0.1f;
    Steering_motor_cmd.K_W = 0.02f;
    Steering_motor_cmd.Pos = steer_pos;
    SERVO_Send_recv(&Steering_motor_cmd, &Steering_motor_fdbck);
}
static void motor_init(RobStrideMotor *m)
{
	(void)RobStride_ReadParam(m, ROBSTRIDE_IDX_VBUS);   HAL_Delay(50U);
	RobStride_ProcessRx();
	(void)RobStride_EnableActiveReport(m, 10U);          HAL_Delay(50U);
	RobStride_ReadAllParams(m);                          HAL_Delay(50U);
	RobStride_ProcessRx();
	(void)RobStride_Stop(m, 1U);                         HAL_Delay(100U);
	(void)RobStride_WriteU8(m, ROBSTRIDE_IDX_RUN_MODE, ROBSTRIDE_MODE_PP_POS); HAL_Delay(50U);
	(void)RobStride_Enable(m);                           HAL_Delay(100U);
	(void)RobStride_WriteFloat(m, ROBSTRIDE_IDX_LIMIT_TRQ, 15.0f); HAL_Delay(20U);
	(void)RobStride_WriteFloat(m, ROBSTRIDE_IDX_VEL_MAX,   10.0f); HAL_Delay(20U);
	(void)RobStride_WriteFloat(m, ROBSTRIDE_IDX_ACC_SET,    3.0f); HAL_Delay(20U);
}
